#include "str.h"
#include <stdio.h> /* ===== DELETE ===== */

int	ft_atoi(char **str)
{
	int	num;

	num = 0;
	while ((**str >= '0' && **str <= '9') && **str)
	{
		num = num * 10 + **str - '0';
		(*str) += 1;
	}
	return (num);
}
